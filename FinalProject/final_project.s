INITIALIZE_STACK:
        movia  sp,STACK_BASE /* Move address of stack pointer */
        ret /*  */
ENABLE_NIOSII_INTERRUPTS:
        /* ctl4 */
        movi   r11,0b1000011 /* IRQ 0 1 6 */
        wrctl  ienable,r11 /* Enable IRQ 0 1 */
        /* ctl0 */
        movi   r11,0b00001 /* PIE bit enables external interrupts */
        wrctl  status,r11  /* Write 1 to PIE bit */
        ret
INITIALIZE_GPIO:
        movi  r2,0b1 /* One */
        
        movia r4,GPIO_RHY /* Load GPIO_RHY address */
        stwio r2,GPIO_DIRECTION(r4) /* Set to output */
        stwio r0,GPIO_DATA(r4) /* Wtire a 1 to GPIO_RHY */

        movia r4,GPIO_HAR /* Load GPIO_HAR address */
        stwio r2,GPIO_DIRECTION(r4) /* Set to output */
        stwio r0,GPIO_DATA(r4) /* Write a 1 to DPIO_HAR */

        ret /* Return from subroutine */

INITIALIZE_KEYS:
        movia r4,KEYS
        movi  r2,0b0111
        stwio r2,KEYS_EDGECAPTURE(r4)
        stwio r2,KEYS_INTMASK(r4)
        ret /* Return from subroutine */

INITIALIZE_TIMER_RHY:
        movia r4,TIMER_RHY /* TIMER_RHY address */
        movi  r2,0b1000 /* Stop bit */
        stwio r2,TIMER_CONTROL(r4) /* Stop TIMER_RHY */
        ldwio r3,(r18) /* Load PERIOD_RHY */
        sthio r3,TIMER_PERIODL(r4) /* Store halfword to PERIODL */
        srli  r3,r3,16 /* Logical shift right 16 bits */
        sthio r3,TIMER_PERIODH(r4) /* Store halfword to PERIODH */
        ret /* Return from subroutine */
INITIALIZE_TIMER_HAR:
        movia r4,TIMER_HAR /* TIMER_HAR address */
        movi  r2,0b1000 /* Stop bit */
        stwio r2,TIMER_CONTROL(r4) /* STOP TIMER HAR */
        ldwio r2,(r19) /* Load PERIOD_HAR */
        sthio r2,TIMER_PERIODL(r4) /* Store halfword to PERIODL */
        srli  r2,r2,16 /* Logical shift right 16 bits */
        sthio r2,TIMER_PERIODH(r4) /* Store halfword to PERIODH */
        ret /* Return from subroutine */

